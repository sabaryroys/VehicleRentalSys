    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(nusr.getText().trim().equals("") || nusr.getText().equals(null))
        {
            JOptionPane.showMessageDialog(this,"User Name cannot be empty","Warning",JOptionPane.WARNING_MESSAGE);
            nusr.grabFocus();
            nusr.setBorder(new LineBorder(Color.red,1));
        }
        else if(npw.getText().trim().equals("") || npw.getText().equals(null))
        {
            JOptionPane.showMessageDialog(this,"Password cannot be empty","Warning",JOptionPane.WARNING_MESSAGE);
            npw.grabFocus();
            npw.setBorder(new LineBorder(Color.red,1));
            nusr.setBorder(new LineBorder(Color.gray,1));
        }
        else if(ncpw.getText().trim().equals("") || ncpw.getText().equals(null))
        {
            JOptionPane.showMessageDialog(this,"Confirm password cannot be blank","Warning",JOptionPane.WARNING_MESSAGE);
            ncpw.grabFocus();
            ncpw.setBorder(new LineBorder(Color.red,1));
            npw.setBorder(new LineBorder(Color.gray,1));
            nusr.setBorder(new LineBorder(Color.gray,1));
        }
        else if(!npw.getText().trim().equals(ncpw.getText().trim()))
        {
             JOptionPane.showMessageDialog(this,"Passwords does not match!","Warning",JOptionPane.WARNING_MESSAGE);
             npw.grabFocus();
             npw.setBorder(new LineBorder(Color.red,1));
             ncpw.setBorder(new LineBorder(Color.red,1));
             nusr.setBorder(new LineBorder(Color.gray,1));
             
        }
        else if(userNameExists(nusr.getText().trim()))
        {
            JOptionPane.showMessageDialog(this,"User name "+nusr.getText()+" is not available. Please enter a new user name","Warning",JOptionPane.WARNING_MESSAGE);
            nusr.grabFocus();
            nusr.setBorder(new LineBorder(Color.red,1));
            ncpw.setText("");
            npw.setText("");
            ncpw.setBorder(new LineBorder(Color.gray,1));
            npw.setBorder(new LineBorder(Color.gray,1));
            
        }
        else
        {
         String   UNameTemp = nusr.getText().trim();
         String PassTemp = ncpw.getText().trim();
         String query = "insert into user_pass values(?,?)";
            try {
                java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/vehicle_db","root","");
                    PreparedStatement stmt = con.prepareStatement(query);
                             stmt.setString(1,UNameTemp);
                                    stmt.setString(2,PassTemp);
                                        stmt.executeUpdate();
                                        JOptionPane.showMessageDialog(null, "User "+UNameTemp+" added successfully", "User Added",JOptionPane.OK_OPTION);
                                           ncpw.setText("");
                                                     npw.setText("");  
                                                        nusr.setText("");
                
                
                
            } catch (SQLException ex) {
                Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
            }
         
            
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

