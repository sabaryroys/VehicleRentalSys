    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(uname.getText().trim().equals(""))
        {
            JOptionPane.showMessageDialog(this,"User name cannot be empty","Warning",JOptionPane.WARNING_MESSAGE);
            uname.grabFocus();
            uname.setBorder(new LineBorder(Color.red,1));
        }
        else if(pwd.getText().trim().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Password cannot be empty","Warning",JOptionPane.WARNING_MESSAGE);
            pwd.grabFocus();
            pwd.setBorder(new LineBorder(Color.red,1));
            uname.setBorder(new LineBorder(Color.gray,1));
        }
        else
        {
        
        String uName = uname.getText();
        String query = "select * from user_pass where uname = 'admin'";
        DbConSelect db = new DbConSelect();
        try {
            rs = db.selectDb(query);
            if(rs.next())
        {
         if(rs.getString(1).trim().equals(uName) && rs.getString(2).trim().equals(pwd.getText().trim()))
         {
             jInternalFrame1.show();
             jInternalFrame1.setSize(250, 400);
             panelControl(false);
            
             
           
              
         }
         else
         {
             JOptionPane.showMessageDialog(this,"Invalid admin credentials","Error",JOptionPane.ERROR_MESSAGE);
             uname.setText("");
             pwd.setText("");
             
         }
         
        }
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

